// Step 1: Select the paragraph element by its ID
const paragraph = document.getElementById("text");

// Step 2: Modify the style of the paragraph

// Change font size
paragraph.style.fontSize = "24px"; // sets the text size

// Change font family
paragraph.style.fontFamily = "Arial, sans-serif"; // changes the font

// Change text color
paragraph.style.color = "blue"; // changes the text color

// Optional: Add other style modifications
paragraph.style.fontWeight = "bold"; // makes the text bold
paragraph.style.backgroundColor = "lightyellow"; // background color
paragraph.style.padding = "10px"; // adds some padding around the text